/**
     * Si le das al checkbox, el bkacground del jpanel se convierte en azul y el
     * texto en blanco y cuando lo deseleccionas, vuelve a su color original
     *
     * @param evt
     */
    private void jAzulActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jAzulActionPerformed
        if (jAzul.isSelected()) {
            jPanel1.setBackground(Color.blue);
            jLabel1.setForeground(Color.white);
            jLabel2.setForeground(Color.white);
        } else {
            jPanel1.setBackground(getBackground());
            jLabel1.setForeground(Color.black);
            jLabel2.setForeground(Color.black);
        }
    }//GEN-LAST:event_jAzulActionPerformed

