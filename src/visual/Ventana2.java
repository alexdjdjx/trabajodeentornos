    /**
     * Método que abre un diálogo cuando le das a la equis y te pregunta si
     * quieres salir, hay dos opciones aceptar y cancelar, aceptar cierra el
     * programa cancelar mantiene el programa
     */
    public void Confirmacion() {
        String[] mensaje = {"aceptar", "cancelar"};
        int entrada = JOptionPane.showOptionDialog(this, "¿Estas seguro de cerrar", null, WIDTH, 3, null, mensaje, mensaje[1]);
        if (JOptionPane.OK_OPTION == entrada) {
            System.exit(0);
        }
    }

